package simple;

public class CharAlpha {

	public static void main(String[] args) {

		char a = '1';
		char b = '2';
		char c = '3';
		char d = 'd';

		if (d == a) {
			System.out.println("The given char is not alphabet");
		} else if (d == b) {
			System.out.println("The given char is not alphabet");
		} else if (d == c) {
			System.out.println("The given char is not alphabet");
		} else {
			System.out.println("The given char is alphabet");
		}
	}

}
